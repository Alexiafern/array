    <P>O operador de concatenação de strings é o ponto .</P>

    <P>Por exemplo:</P>

    <P>$ddd  = '34';</P>
    <P>$fone = '32105691';</P>

    <P>echo '(' . $ddd . ') ' . $fone;</P>
    <P>Esse exemplo funciona igual com aspas duplas:</P>

    <P>echo "(" . $ddd . ") " . $fone;</P>
    <P>É assim que você concatena strings em PHP.</P>
