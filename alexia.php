        <h1>O que é concatenação no PHP?</h1>

        <div class="box">O operador de concatenação de strings é o ponto .</div>

            <br>

            <P>Por exemplo:</P>

            <P>$ddd  = '34';</P>
            <P>$fone = '32105691';</P>

            <P>echo '(' . $ddd . ') ' . $fone;</P>
            <P>Esse exemplo funciona igual com aspas duplas:</P>

            <P>echo "(" . $ddd . ") " . $fone;</P>
            <P>É assim que você concatena strings em PHP.</P>
            <h2>Exemplos de concatenação em PHP:</h2>
    
                    $a = "Hello ";
                    $b = $a . "World!";
                    $a = "Hello ";
                    $a .= "World!";...
                    
